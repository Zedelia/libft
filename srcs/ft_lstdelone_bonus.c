#include "../includes/libft.h"

/*
** Libère la mémoire de l’élément passé en argumenten utilisant
** la fonction del puis avec free(3). La mémoire de next ne doit pas être free.
*/

void	ft_lstdelone(t_list *lst, void (*del)(void*))
{
	if (lst && del)
	{
		del(lst->content);
		free(lst);
		lst = NULL;
	}
}
